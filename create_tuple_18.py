# 1. Creating a tuple using parentheses ()
tuple1 = (1, 2, 3, 4, 5)
print("Tuple using parentheses:", tuple1)

# 2. Creating a tuple without parentheses (comma-separated values)
tuple2 = 10, 20, 30
print("Tuple without parentheses:", tuple2)

# 3. Creating a tuple using the tuple() constructor from another iterable
tuple3 = tuple([100, 200, 300])  # From list
print("Tuple from list using tuple():", tuple3)

# 4. Creating a tuple from a string using tuple()
tuple4 = tuple("hello")
print("Tuple from string using tuple():", tuple4)

