# 1. Creating an empty list
empty_list = []
print("Empty list:", empty_list)

# 2. Creating a list with elements
numbers = [1, 2, 3, 4, 5]
print("List of numbers:", numbers)

# 3. Using the list() constructor with an iterable (like a string)
chars = list("hello")
print("List from string:", chars)

# 4. Using range() and converting to list
range_list = list(range(1, 10, 2))
print("List created from range:", range_list)

