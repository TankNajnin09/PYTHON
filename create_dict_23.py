# 1. Using curly braces {}
dict1 = {'name': 'Alice', 'age': 25, 'city': 'New York'}
print("Dictionary using {}:", dict1)

# 2. Using the dict() constructor with keyword arguments
dict2 = dict(name='Bob', age=30, city='London')
print("\nDictionary using dict() with keyword arguments:", dict2)

# 3. Creating an empty dictionary and adding items later
dict3 = {}
dict3['name'] = 'Eva'
dict3['age'] = 22
print("\nCreating empty dictionary and adding items:", dict3)
